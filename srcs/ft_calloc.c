#include "../libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;

	if (!(ptr = malloc(size * count))
	|| count == 0 || size == 0)
		return (NULL);
	ft_bzero(ptr, size * count);
	return (ptr);
}
